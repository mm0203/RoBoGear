#include "Effect.h"

//=============================================================================
// 
// 初期化処理
// 
//=============================================================================
void Effect::Init()
{
	// メンバ変数初期化
	m_FrameNum = XMINT2(1, 1);
	m_MaxAnimNum = 0;
	m_CurrentAnimNum = 0;

	BillBoard::Init();
}

//=============================================================================
// 
// 終了処理
// 
//=============================================================================
void Effect::Uninit()
{
	BillBoard::Uninit();
}

//=============================================================================
// 
// 更新処理
// 
//=============================================================================
void Effect::Update()
{
	if (++m_SpeedCount >= m_Speed)
	{
		if (m_CurrentAnimNum++ > m_MaxAnimNum)
		{
			EffectManager::DeleteObject(this);
		}
		m_SpeedCount = 0;
	}

	BillBoard::Update();
}

//=============================================================================
// 
// 描画処理
// 
//=============================================================================
void Effect::Draw()
{
	BillBoard::Draw(m_CurrentAnimNum);
}



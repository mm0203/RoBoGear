#include "StageSelectLogo.h"

//=============================================================================
// 
// 無名名前空間
// 
//=============================================================================
namespace
{
	const auto StageSelectLogo = L"data/texture/StageSelect/StageSelect.png";
}

//=============================================================================
// 
// コンストラクタ
// 
//=============================================================================
CStageSelectLogo::CStageSelectLogo()
{
	m_TextureMove = std::make_shared<CTextureMoveComponent>();
}

//=============================================================================
// 
// 初期化処理
// 
//=============================================================================
void CStageSelectLogo::Init()
{
	m_Polygon->SetSize(SCREEN_WIDTH, 500);
	m_Polygon->LoadTexture(StageSelectLogo);
}

//=============================================================================
// 
// 終了処理
// 
//=============================================================================
void CStageSelectLogo::Uninit()
{
}

//=============================================================================
// 
// 更新処理
// 
//=============================================================================
void CStageSelectLogo::Update()
{
	m_TextureMove->TextureUpDown(m_Polygon, 250.0f);
}

//=============================================================================
// 
// 描画処理
// 
//=============================================================================
void CStageSelectLogo::Draw()
{
	m_Polygon->Draw();
}
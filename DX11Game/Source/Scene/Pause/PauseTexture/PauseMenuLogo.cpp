#include "PauseMenuLogo.h"

//=============================================================================
// 
// 無名名前空間
// 
//=============================================================================
namespace
{
	const auto PauseLogo = L"data/TEXTURE/Pause/Pause.png";
}

//=============================================================================
// 
// コンストラクタ
// 
//=============================================================================
CPauseMenuLogo::CPauseMenuLogo()
{
	m_TextureMove = std::make_shared<CTextureMoveComponent>();
}

//=============================================================================
// 
// 初期化処理
// 
//=============================================================================
void CPauseMenuLogo::Init()
{
	// ポーズロゴ
	m_Polygon->SetSize(SCREEN_WIDTH, 500);
	m_Polygon->LoadTexture(PauseLogo);
}

//=============================================================================
// 
// 終了処理
// 
//=============================================================================
void CPauseMenuLogo::Uninit()
{
}

//=============================================================================
// 
// 更新処理
// 
//=============================================================================
void CPauseMenuLogo::Update()
{
	m_TextureMove->TextureUpDown(m_Polygon, 250.0f);
}

//=============================================================================
// 
// 描画処理
// 
//=============================================================================
void CPauseMenuLogo::Draw()
{
	m_Polygon->Draw();
}
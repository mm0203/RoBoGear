#include "GameOverLogo.h"

//=============================================================================
// 
// 無名名前空間
// 
//=============================================================================
namespace
{
	const auto GameOverLogo = L"data/texture/GameOver/GameOver.png";
}

//=============================================================================
// 
// コンストラクタ
// 
//=============================================================================
CGameOverLogo::CGameOverLogo()
{
	m_TextureFade = std::make_shared<CTextureFadeComponent>();
}

//=============================================================================
// 
// 初期化処理
// 
//=============================================================================
void CGameOverLogo::Init()
{
	m_Polygon->SetSize(SCREEN_WIDTH, SCREEN_HEIGHT);
	m_Polygon->LoadTexture(GameOverLogo);
	m_Polygon->SetAlpha(0);
}

//=============================================================================
// 
// 終了処理
// 
//=============================================================================
void CGameOverLogo::Uninit()
{
}

//=============================================================================
// 
// 更新処理
// 
//=============================================================================
void CGameOverLogo::Update()
{
	m_TextureFade->FadeInOut(m_Polygon);
}

//=============================================================================
// 
// 描画処理
// 
//=============================================================================
void CGameOverLogo::Draw()
{
	m_Polygon->Draw();
}
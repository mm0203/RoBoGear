#include "TitleLogo.h"

//=============================================================================
// 
// 無名名前空間
// 
//=============================================================================
namespace
{
	const auto TitleLogo = L"data/texture/Title/titlelogo.png";
}

//=============================================================================
// 
// コンストラクタ
// 
//=============================================================================
CTitleLogo::CTitleLogo()
{
	m_TextureMove = std::make_unique<CTextureMoveComponent>();
}

//=============================================================================
// 
// 初期化処理
// 
//=============================================================================
void CTitleLogo::Init()
{
	// タイトル
	m_Polygon->SetSize(SCREEN_WIDTH, 500.0f);
	m_Polygon->LoadTexture(TitleLogo);
}

//=============================================================================
// 
// 終了処理
// 
//=============================================================================
void CTitleLogo::Uninit()
{
}

//=============================================================================
// 
// 更新処理
// 
//=============================================================================
void CTitleLogo::Update()
{
	// テキスト上下
	m_TextureMove->TextureUpDown(m_Polygon, 200.0f);
}

//=============================================================================
// 
// 描画処理
// 
//=============================================================================
void CTitleLogo::Draw()
{
	m_Polygon->Draw();
}
#include "Cursor.h"

#define MODEL_SELECT "data/MODEL/Object/Arrow_symbol_01/Arrow_symbol_01.fbx"

//=============================================================================
// 
// コンストラクタ
// 
//=============================================================================
CCursor::CCursor()
{
	m_Pos = XMFLOAT3();
	m_Scale = XMFLOAT3(1.0f, 1.0f, 1.0f);
	m_Rot = XMFLOAT3();
}

//=============================================================================
// 
// 初期化処理
// 
//=============================================================================
void CCursor::Init()
{
	m_Model.LoadModel(MODEL_SELECT);
}

//=============================================================================
// 
// 終了処理
// 
//=============================================================================
void CCursor::Uninit()
{

}

//=============================================================================
// 
// 更新処理
// 
//=============================================================================
void CCursor::Update(XMFLOAT3 pos, XMFLOAT3 scale, XMFLOAT3 rot)
{
	m_Pos = pos;
	m_Rot = rot;
	m_Pos.y += 170.0f;
	m_Rot.x += 90.0f;
}

//=============================================================================
// 
// 描画処理
// 
//=============================================================================
void CCursor::Draw()
{
	m_Model.Draw(m_Pos, m_Scale, m_Rot);
}


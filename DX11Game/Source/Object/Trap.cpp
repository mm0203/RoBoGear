#include "Trap.h"
#include <Renderer/Effect/Effect.h>

//=============================================================================
// 
// コンストラクタ
// 
//=============================================================================
CTrap::CTrap()
{
	m_tag = "Trap";
	m_Model.LoadModel(MODEL_TRAP);
	m_nCount = 0;
}

//=============================================================================
// 
// 初期化処理
// 
//=============================================================================
void CTrap::Init()
{

}

//=============================================================================
// 
// 更新処理
// 
//=============================================================================
void CTrap::Update()
{
	m_nCount++;
	// TODO 時間
	// 一定時間でエフェクト
	if (m_nCount >= 60 * 3)
	{
		EffectManager::CreateEffect(Effect_Trap, m_Pos);
		m_nCount = 0;
	}
}


//=============================================================================
void CGenerare::Update()
{
	// プレイヤーの座標と同じ場所に
	const auto& player = CObjectManager::SearchObjectTag(TagPlayer);
	m_Pos = player.lock()->GetPos();
	m_Scale = player.lock()->GetScale();
	m_Rot = player.lock()->GetRot();
}

#include "TextureColorChangeComponent.h"
#include <cmath>

//=============================================================================
// 
// コンストラクタ
// 
//=============================================================================
CTextureColorChangeComponent::CTextureColorChangeComponent()
{
	m_nTime = 10;
	m_fColor = 0.0f;
}

//=============================================================================
// 
// テクスチャ色変更
// 
//=============================================================================
float CTextureColorChangeComponent::ColorChange()
{
	m_nTime += 3;
	m_fColor = abs(sinf(m_nTime * 3.1415f / 100.0f));
	return m_fColor;
}
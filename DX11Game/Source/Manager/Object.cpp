#include "ObjectManager.h"

//=============================================================================
// 
// 無名名前空間
// 
//=============================================================================
namespace
{
	// 慣性
	const float MoveRate = 0.1f;
}

//=============================================================================
// 
// コンストラクタ
// 
//=============================================================================
CObject::CObject()
{
	m_Pos = XMFLOAT3();
	m_Coord = XMINT2();
	m_Scale = XMFLOAT3();
	m_Rot = XMFLOAT3();
	m_Move = XMFLOAT3();
	m_tag = "\0";
}
//=============================================================================
// 
// 終了
// 
//=============================================================================
void CObject::Uninit()
{
	m_Model.ReleaseModel();
}

//=============================================================================
// 
// 描画
// 
//=============================================================================
void CObject::Draw()
{
	m_Model.Draw(m_Pos, m_Scale, m_Rot);
}

//=============================================================================
// 
// オブジェクト移動
// 
//=============================================================================
void CObject::ObjectMove()
{
	// 移動
	m_Pos += m_Move;
	// 慣性
	m_Move += (0.0f - m_Move) * MoveRate;
}
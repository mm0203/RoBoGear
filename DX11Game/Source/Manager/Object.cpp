#include "ObjectManager.h"


//=============================================================================
// 
// 無名名前空間
// 
//=============================================================================
namespace
{
	float MoveRate = 0.1f;		// 慣性
}

//=============================================================================
// 
// コンストラクタ
// 
//=============================================================================
Object::Object()
{
	m_Pos = XMFLOAT3();
	m_Coord = XMINT2();
	m_Scale = XMFLOAT3();
	m_Rot = XMFLOAT3();
	m_Move = XMFLOAT3();
	m_tag = "Object";
}
//=============================================================================
// 
// 終了
// 
//=============================================================================
void Object::Uninit()
{
	m_Model.ReleaseModel();
}

//=============================================================================
// 
// 描画
// 
//=============================================================================
void Object::Draw()
{
	m_Model.Draw(m_Pos, m_Scale, m_Rot);
}

//=============================================================================
// 
// オブジェクト移動
// 
//=============================================================================
void Object::ObjectMove()
{
	// 移動
	m_Pos += m_Move;
	// 慣性
	m_Move += (0.0f - m_Move) * MoveRate;
}